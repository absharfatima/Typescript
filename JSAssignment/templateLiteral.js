// Task 8: Using template literals and multiline strings

// Variables
const name = "Alice";
const age = 25;
const occupation = "Software Engineer";

// Creating a string using template literals
const introduction = `Hello, my name is ${name}.
I am ${age} years old, and I work as an ${occupation}.`;

// Logging the result
console.log(introduction);

// Output:
// Hello, my name is Alice.
// I am 30 years old, and I work as an Engineer.
