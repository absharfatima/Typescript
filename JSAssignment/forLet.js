// Example iterable (an array in this case)
const numbers = [1, 2, 3, 4, 5];

// Using the let keyword in a for...of loop
for (let value of numbers) {
  console.log(value);
}



// Example string
const message = "Hello, World!";

// Using the let keyword in a for...of loop to iterate over characters in the string
for (let char of message) {
  console.log(char);
}
